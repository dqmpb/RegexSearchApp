    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     Pattern pat;
        if(jRadioButton1.isSelected()){
       pat = Pattern.compile(jTextField1.getText().toString());
     }
     else{
       pat = Pattern.compile(jTextField1.getText().toString(), Pattern.CASE_INSENSITIVE);
     }
      Matcher mat = pat.matcher(jTextArea1.getText().toString());
      if(mat.find()){
      jLabel3.setText("OK");
      jLabel3.setForeground(Color.green);
      jLabel3.show();
      }  
      else{
      jLabel3.setText("NG");
       jLabel3.setForeground(Color.red);
      jLabel3.show();
      }
          
        
    }//GEN-LAST:event_jButton1ActionPerformed

